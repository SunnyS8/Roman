let seeded = false;

export function seedKnowledge(): void {
  if (seeded) return;

  const existing = getAllKnowledge();
  const existingInsights = new Set(existing.map((e) => e.insight));

  let added = 0;
  for (const entry of ENTRIES) {
    if (!existingInsights.has(entry.insight)) {
      addKnowledge(
        { topic: entry.topic, insight: entry.insight, source: "knowledge_seed" },
        0.9,
      );
      added++;
    }
  }

  seeded = true;
  if (added > 0) {
    console.log(`🧠 Добавлено знаний об Академии Роста: ${added}`);
  }
}
